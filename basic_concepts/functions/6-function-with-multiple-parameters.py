# Function that accepts two parameters (for Chicken & Sauce)
def dish_name(chicken, sauce):
    # Prints the food item
    print("The food is:", chicken)
    
    # Prints the sauce and its description
    print("The sauce is:", sauce, "and is spicy!")

# Calls function with two string inputs for Fried Chicken and Hot Sauce
dish_name("Fried Chicken", "Hot Sauce")
