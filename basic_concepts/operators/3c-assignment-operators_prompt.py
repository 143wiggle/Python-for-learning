# Assignment operators with user input
a = int(input("Enter a number: "))
b = int(input("Enter another number: "))

a += b  # a = a + b
print(f"a += b: {a}")

a -= b  # a = a - b
print(f"a -= b: {a}")

a *= b  # a = a * b
print(f"a *= b: {a}")

a /= b  # a = a / b
print(f"a /= b: {a}")

a %= b  # a = a % b
print(f"a %= b: {a}")
